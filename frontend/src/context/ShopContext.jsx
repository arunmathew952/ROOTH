import { createContext, useState , useEffect } from "react";
import { toast } from "react-toastify";
import { useNavigate } from "react-router-dom";
import axios from 'axios'

export const ShopContext = createContext();

const ShopContextProvider = (props) => {

    const currency = "₹";
    const delivery_fee = 50;
    const backendUrl = import.meta.env.VITE_BACKEND_URL
    const [search,setSearch] = useState('');
    const [showSearch,setShowSearch] = useState(false)
    const [cartItems,setCartItems] = useState({});
    const [products, setProducts] = useState([]);
    const [token,setToken] =useState('')
    const navigate = useNavigate();

    const addToCart = async (itemId,size) => {

        if (!size){
            toast.error("Please select a size");
            return;
        }
        let cartData = structuredClone(cartItems);

        if (cartData[itemId]){
            if (cartData[itemId][size]) {
                cartData[itemId][size] += 1;
            }else{
                cartData[itemId][size] = 1;
            }
        }else{
            cartData[itemId] = {};
            cartData[itemId][size] = 1;
        }
        setCartItems(cartData);
            if (token){
            try {
                await axios.post(backendUrl + '/api/cart/add', {itemId,size}, {headers:{token}})
            } catch (error) {
                console.log(error)                   
                toast.error(error.message)
                    
            }
        }
    }
    const getCartCount = () => {
        let totalCount = 0;
        for (const items in cartItems){
            for (const item in cartItems[items]){
                try {
                    if (cartItems[items][item]>0){
                        totalCount += cartItems[items][item];
                    }
                } catch (error) {
                    console.error("Error getting cart count:", error);
                }
            }
           
        }
        return totalCount;
    }
    const updateQuantity = async (itemId, size, quantity) => {
        let cartData = structuredClone(cartItems);
    
        // Case 1: When quantity becomes 0 → delete the product
        if (quantity === 0) {
            delete cartData[itemId][size];
            if (Object.keys(cartData[itemId]).length === 0) {
                delete cartData[itemId];
            }
    
            setCartItems(cartData);
    
            if (token) {
                try {
                    await axios.post(
                        backendUrl + "/api/cart/remove",
                        { itemId, size },
                        { headers: { token } }
                    );
                } catch (error) {
                    console.log(error);
                    toast.error(error.message);
                }
            }
            return;
        }
    
        // Case 2: Normal quantity update
        cartData[itemId][size] = quantity;
        setCartItems(cartData);
    
        if (token) {
            try {
                await axios.post(
                    backendUrl + "/api/cart/update",
                    { itemId, size, quantity },
                    { headers: { token } }
                );
            } catch (error) {
                console.log(error);
                toast.error(error.message);
            }
        }
    };
    

    const getCartAmount = () => {
        let totalAmount = 0;
        for (const items in cartItems) {
            let itemInfo = products.find((product) => product._id === items);
            if (!itemInfo) continue; // Make sure product exists
            for (const item in cartItems[items]) {
                try {
                    if (cartItems[items][item] > 0) {
                        totalAmount += itemInfo.price * cartItems[items][item];
                    }
                } catch (error) {
                    // Optionally handle/log the error
                }
            }
        }
        return totalAmount;
    };

    const getProductsData = async () => {
        try {
            
            const response = await axios.get(backendUrl + '/api/product/list')
            if (response.data.success){
                setProducts(response.data.products)
            }else{
                toast.error(response.data.message)
            }
        } catch (error) {
            console.log(error)
            toast.error(error.message)

            
        }
    }
    useEffect (()=>{
        getProductsData()
    },[])

    useEffect(()=>{
        if (!token && localStorage.getItem('token')){
            setToken(localStorage.getItem('token'))
        }
    })
    const getUserCart = async () => {
        if (!token) return;
    
        try {
            const response = await axios.post(
                backendUrl + "/api/cart/get",
                {},
                { headers: { token } }
            );
    
            if (response.data.success) {
                setCartItems(response.data.cartData || {});
            } else {
                toast.error(response.data.message);
            }
    
        } catch (error) {
            console.log(error);
            toast.error(error.message);
        }
    };
    

    useEffect(() => {
        if (token) {
            getUserCart();
        }
    }, [token]);

    const removeFromCart = async (itemId, size) => {
        let cartData = structuredClone(cartItems);
    
        delete cartData[itemId][size];
    
        if (Object.keys(cartData[itemId]).length === 0) {
            delete cartData[itemId];
        }
    
        setCartItems(cartData);
    
        if (token) {
            try {
                await axios.post(backendUrl + "/api/cart/remove",
                    { itemId, size },
                    { headers: { token } }
                );
            } catch (error) {
                console.log(error);
                toast.error(error.message);
            }
        }
    };
    
    
    
    


    const value ={
        products,currency,delivery_fee,
        search,setSearch,showSearch,backendUrl,setShowSearch,cartItems,getProductsData,addToCart,
        getCartCount,updateQuantity,removeFromCart,getCartAmount,navigate,token,setToken
    }
    return(
        <ShopContext.Provider value={value}>
            {props.children}
        </ShopContext.Provider>
    )
}

export default ShopContextProvider;